package Employee;

class Certificated {
    private int certificatedID;
    private String certificateName, CertificateRank, CertificatedDat;

    public int getCertificatedID() {
        return certificatedID;
    }

    public void setCertificatedID(int certificatedID) {
        this.certificatedID = certificatedID;
    }

    public String getCertificateName() {
        return certificateName;
    }

    public void setCertificateName(String certificateName) {
        this.certificateName = certificateName;
    }

    public String getCertificateRank() {
        return CertificateRank;
    }

    public void setCertificateRank(String certificateRank) {
        CertificateRank = certificateRank;
    }

    public String getCertificatedDat() {
        return CertificatedDat;
    }

    public void setCertificatedDat(String certificatedDat) {
        CertificatedDat = certificatedDat;
    }
}
